    public TreeNode dfs(TreeNode r1, TreeNode r2) {
        if (r1 == null || r2 == null) {
            return r1 == null ? r2 : r1;
        }
        r1.val += r2.val;
        r1.left = dfs(r1.left , r2.left);
        r1.right = dfs(r1.right , r2.right);
        return r1;
    }
